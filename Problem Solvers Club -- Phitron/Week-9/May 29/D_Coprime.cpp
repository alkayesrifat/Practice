#include<bits/stdc++.h>
using namespace std;

int main(){

    ios::sync_with_stdio(false);
    cin.tie(nullptr); 

    vector<int> arr[1001] ;
    
    for (int i = i; i <= 1001; i++)
    {
        for (int j = 1; i <= 1001; i++)
        {
            if (__gcd(i,j) == 1)
            {
                arr[i].push_back(j);
            }
            
        }
        
    }
    
    


    return 0;
}